print("\nPara calcular el area de un triangulo se necesitan la altura y la base, \ningrese solamente valores numericos cuando se soliciten\n")

altura = float(input("Ingrese el valor de altura -> "))
base = float(input("Ingrese el valor de la base -> "))
area= altura*base/2

print("\nEl area de tu triangulo es igual a altura*base/2 = " + str(area))
